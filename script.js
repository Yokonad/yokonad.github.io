let currentFrame = 0;
let isAnimating = true;

function animateAscii() {
    const preElement = document.querySelector('header h1 pre');
    
    if (!preElement) return;
    
    function showNextFrame() {
        if (currentFrame < asciiFrames.length - 1) {
            preElement.textContent = asciiFrames[currentFrame];
            currentFrame++;
            setTimeout(showNextFrame, 200); // 200ms entre cada letra
        } else {
            // Mostrar el frame completo
            preElement.textContent = asciiFrames[asciiFrames.length - 1];
            // Esperar 3 segundos antes de reiniciar
            setTimeout(() => {
                currentFrame = 0;
                showNextFrame();
            }, 3000);
        }
    }
    
    showNextFrame();
}

// Iniciar la animación cuando cargue la página
document.addEventListener('DOMContentLoaded', animateAscii);
